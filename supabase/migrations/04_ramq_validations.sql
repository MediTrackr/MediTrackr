  ADD CONSTRAINT check_billing_deadline
  CHECK (
    status = 'draft'
    OR submitted_at IS NULL
    OR submitted_at <= (service_date + INTERVAL '90 days')
  );

-- =====================================================
-- 4. PEDIATRIC AUTO-TRIGGER
-- Automatically sets age_at_service and is_pediatric
-- when a claim has patient_dob and service_date
-- =====================================================
CREATE OR REPLACE FUNCTION public.ramq_set_pediatric()
RETURNS TRIGGER AS $$
BEGIN
  IF NEW.patient_dob IS NOT NULL AND NEW.service_date IS NOT NULL THEN
    NEW.age_at_service := DATE_PART('year', AGE(NEW.service_date::date, NEW.patient_dob::date))::INTEGER;
    -- RAMQ: pediatric majoration applies under 2 years old (Annexe II)
    NEW.is_pediatric := NEW.age_at_service < 2;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_ramq_pediatric ON public.ramq_claims;
CREATE TRIGGER trg_ramq_pediatric
  BEFORE INSERT OR UPDATE OF patient_dob, service_date
  ON public.ramq_claims
  FOR EACH ROW
  EXECUTE FUNCTION public.ramq_set_pediatric();

-- =====================================================
-- 5. LOCALITY VALIDATION FUNCTION
-- Returns TRUE if the code is valid or NULL (not provided)
-- =====================================================
CREATE OR REPLACE FUNCTION public.ramq_validate_locality(p_code TEXT)
RETURNS BOOLEAN AS $$
BEGIN
  IF p_code IS NULL OR p_code = '' THEN RETURN TRUE; END IF;
  -- Code 60000 = valid "hors Quebec" code per GuideOmni section 5.2.2
  IF p_code = '60000' THEN RETURN TRUE; END IF;
  RETURN EXISTS (SELECT 1 FROM public.localities WHERE locality_code = p_code);
END;
$$ LANGUAGE plpgsql STABLE;

-- =====================================================
-- 6. PROVIDER NUMBER VALIDATION FUNCTION
-- Per "Numeros attribues par la RAMQ":
--   1XXXXX = omnipraticiens et specialistes
--   5XXXXX = medecins residents
-- =====================================================
CREATE OR REPLACE FUNCTION public.ramq_validate_provider_number(p_num TEXT)
RETURNS BOOLEAN AS $$
BEGIN
  IF p_num IS NULL OR p_num = '' THEN RETURN TRUE; END IF;
  -- Must be 6 digits starting with 1 (specialist/omni) or 5 (resident)
  RETURN p_num ~ '^[15][0-9]{5}$';
END;
$$ LANGUAGE plpgsql IMMUTABLE;
